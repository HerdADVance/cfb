                        <h1 class="team-title">Texas Longhorns</h1>
                        <p>Is Texas Back?&trade; Joe Tessitore certainly jumped the gun after the Longhorns' thrilling overtime defeat of Notre Dame back on Labor Day weekend in 2016, but it appears that Tom Herman has finally turned his team into a legitimate Big XII and national player for the forseeable future.</p>
                        <p>Texas's offense didn't provide fantasy players with an abundance of options in 2018, but it did produce three absolute studs in sophomore QB <strong>Sam Ehlinger</strong> and his top two targets &mdash; juniors <strong>Lil'Jordan Humphrey</strong> and <strong>Collin Johnson</strong>. Humphrey is off to the NFL, but Ehlinger and Johnson return as elite 2019 fantasy targets at their respective positions.</p>
                        <p>As mentioned, there wasn't much to get excited about after those three. Running back was almost a perfect time share between senior <strong>Tre Watson</strong> and true freshman <strong>Keaontay Ingram</strong>, and Ehlinger hogged too many of the rushing TD's for either to provide consistent production. The good news is that Ingram was the much more talented back and could very well be a true bellcow as a sophomore in 2019.</p>
                        <p>Speedster junior WR <strong>Devin Duvernay</strong> was at times a decent flex option as the team's #3 WR. The big question for his finale in Austin is will he approach anything close to Johnson's #2 production, or will his 5'11 size keep him as the third option while one of the Longhorns' talented young 4-stars steps into the Johnson role? Senior TE <strong>Andrew Beck</strong> was the starter all year but only topped 30 yards in one game. Don't count on his replacement to be a viable fantasy option in 2019.</p>
